/**
 * Classe que representa a entidade usuário
 */
export class User {
  /**
   * ID do usuário
   */
  public id?: string;

  /**
   * Nome do usuário
   */
  public name?: string;

  /**
   * Email do usuário
   */
  public email?: string;

  /**
   * Senha do usuário
   */
  public password?: string;

  /**
   * Cargo do usuário
   */
  public role?: string;
}
